public class DataRanges {
    public static void main(String[] args) {
        System.out.println("Byte minimum: " + Byte.MIN_VALUE);
        System.out.println("Byte maximum: " + Byte.MAX_VALUE);
        System.out.println("Byte bytes: " + Byte.BYTES);
        System.out.println("Int maximum: " + Integer.MIN_VALUE);
        System.out.println("Int minimum: " + Integer.MAX_VALUE);
        System.out.println("Int bytes: " + Integer.BYTES);
        System.out.println("Float maximum: " + Float.MIN_VALUE);
        System.out.println("Float minimum: " + Float.MAX_VALUE);
        System.out.println("Float bytes: " + Float.BYTES);
    }
}
